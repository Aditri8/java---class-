public class ArrayLength {
    public static void main(String args[]){
        int emptyArr[] = new int[10];
        int a[] = {1, 2, 3, 4};
        double b[] = {1.2, 1.3, 2.2};

        System.out.println("Length of 1st array: " +emptyArr.length);
        System.out.println("Length of 2nd array: " +a.length);
        System.out.println("Length of 3rd array: " +b.length);
    }
}
